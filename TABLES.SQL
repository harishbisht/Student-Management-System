CREATE TABLE BATCHES
( BCODE  VARCHAR(15)  PRIMARY KEY,
   SUBJECT  VARCHAR(10),
   STDATE    DATE,
   ENDDATE  DATE,
   TIMING    VARCHAR(15),
   NOSESSIONS  NUMBER(2),
   TFEE        NUMBER(5),
   LFEE        NUMBER(4)
);

CREATE TABLE STUDENTS
(  BCODE  VARCHAR(15)  REFERENCES BATCHES,
   ROLLNO  NUMBER(3),
   SNAME  VARCHAR(30),
   FNAME   VARCHAR(30),
   ADDRESS  VARCHAR(100),
   PHONENO  VARCHAR(20),
   EMAIL        VARCHAR(30),
   QUAL          VARCHAR(10),
   CURROCUP VARCHAR(20),
   DJ               DATE,
   FEEPAID    NUMBER(5),
   PRIMARY KEY( BCODE,ROLLNO)
);

CREATE TABLE PAYMENTS
( RCPTNO  NUMBER(5) PRIMARY KEY,
   BCODE    VARCHAR(15),
   ROLLNO  NUMBER(3),
   AMT        NUMBER(5),
   DP           DATE,
   TOWARDS  CHAR(1),
   FOREIGN KEY (BCODE,ROLLNO) 
     REFERENCES  STUDENTS (BCODE,ROLLNO)
);


  
   